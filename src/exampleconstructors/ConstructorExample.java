package exampleconstructors;

public class ConstructorExample {
	
	private int id;
	
	public ConstructorExample() {
		
		this.id = 79990;
		this.id = 7790;
		
	}

	public static void main(String[] args) {
		
		ConstructorExample Obj1 = new ConstructorExample();
		System.out.println(Obj1.id);
		

	}

}
